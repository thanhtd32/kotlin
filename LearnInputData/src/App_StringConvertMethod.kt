fun main(args: Array<String>) {
    var x1:Boolean="true".toBoolean()
    println(x1)

    var x2:Byte="3".toByte()
    println(x2)

    var x3:Short="30".toShort()
    println(x3)

    var x4:Int="15".toInt()
    println(x4)

    var x5:Long="100".toLong()
    println(x5)

    var x6:Float="15.5".toFloat()
    println(x6)

    var x7:Double="15.5".toDouble()
    println(x7)
}